public class Atividade2 {
    public static void main(String[] args) {

        Integer nota = 3;

        //notas
        if (nota >= 9) {
            System.out.println("Exelente");
        }else if (nota >= 7) {
            System.out.println("Bom");
        }else if (nota >= 5) {
            System.out.println("Satisfatorio");
        }else if (nota < 5) {
            System.out.println("Insatisfatorio");
        }

    }
}
